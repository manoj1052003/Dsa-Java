package com.opr;

public class elseifblock {

	public static void main(String[] args) {
		int per = 52;
		if(per >=85) {
			System.out.println("dist");
		}
		else if(per >=60) {
			System.out.println("first");
		}
		else if(per >=50) {
			System.out.println("second");
		}
		else if(per >=35) {
			System.out.println("pass");
		}
		else {
			System.out.println("fail");
		}

	}

}
